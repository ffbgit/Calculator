var Calculator = {
  multiply: function(number1, number2) {
    return number1 * number2;
  },

  divide: function(number1, number2) {
    return number1/number2;
  },

  countDown: function(number1, number2) {
    return number1-number2;
  },

  addUp: function(number1, number2) {
    return (number1+number2);
  }
}
